#ifndef LOG_H
#define LOG_H

/*
 * ============================================
 * 日志系统头文件 - log.h
 * ============================================
 * 
 * 功能概述：
 * 1. 支持同步和异步两种日志写入模式
 * 2. 使用单例模式确保全局唯一实例
 * 3. 实现按天、按行自动滚动日志文件
 * 4. 提供四种日志级别：DEBUG、INFO、WARN、ERROR
 * 5. 通过阻塞队列实现异步日志写入，提高性能
 */

#include <stdio.h>
#include <iostream>
#include <string>
#include <stdarg.h>
#include <pthread.h>
#include "block_queue.h"

using namespace std;

class Log
{
public:
    /*
     * =====================================================
     * 单例模式实现
     * =====================================================
     * 
     * 设计思想：
     * - 使用静态局部变量实现懒汉式单例
     * - C++11 标准保证静态局部变量的初始化是线程安全的
     * - 因此不需要手动加锁，既安全又高效
     * 
     * 优势：
     * - 全局只有一个 Log 实例，保证日志记录的统一性
     * - 延迟初始化，节省资源
     * - 自动管理内存，无需手动释放
     */
    static Log *get_instance()
    {
        static Log instance;
        return &instance;
    }

    /*
     * =====================================================
     * 异步写日志线程函数
     * =====================================================
     * 
     * 功能：
     * - 这是异步日志写入的线程回调函数
     * - 由 pthread_create 创建线程时调用
     * 
     * 工作流程：
     * 1. 获取 Log 单例实例
     * 2. 调用 async_write_log() 方法
     * 3. 该方法会进入无限循环，持续从阻塞队列中取出日志
     * 
     * 参数 args：
     * - 本实现中未使用，保留为线程回调的标准参数
     */
    static void *flush_log_thread(void *args)
    {
        Log::get_instance()->async_write_log();
    }

    /*
     * =====================================================
     * 日志系统初始化函数
     * =====================================================
     * 
     * 参数说明：
     * - file_name: 日志文件名（可包含路径）
     * - close_log: 是否关闭日志（1=关闭，0=开启）
     *              生产环境可通过此参数关闭日志，提升性能
     * - log_buf_size: 日志缓冲区大小，默认 8192 字节
     *                 用于临时存储格式化后的日志内容
     * - split_lines: 日志文件最大行数，默认 5000000 行
     *               当行数达到此值时，自动创建新文件
     * - max_queue_size: 阻塞队列最大长度
     *                   0: 同步模式，日志直接写入文件
     *                   >0: 异步模式，创建阻塞队列并启动异步线程
     * 
     * 返回值：
     * - true: 初始化成功
     * - false: 初始化失败（如文件打开失败）
     * 
     * 初始化逻辑：
     * 1. 判断 max_queue_size，决定同步/异步模式
     * 2. 异步模式：创建阻塞队列，启动异步写入线程
     * 3. 分配日志缓冲区内存
     * 4. 根据当前日期生成日志文件名（格式：YYYY_MM_DD_filename）
     * 5. 打开日志文件，准备写入
     */
    bool init(const char *file_name, int close_log, int log_buf_size = 8192, int split_lines = 5000000, int max_queue_size = 0);

    /*
     * =====================================================
     * 写日志核心函数
     * =====================================================
     * 
     * 参数说明：
     * - level: 日志级别
     *         0: DEBUG - 调试信息
     *         1: INFO  - 一般信息
     *         2: WARN  - 警告信息
     *         3: ERROR - 错误信息
     * - format: 格式化字符串（类似 printf 的格式）
     * - ...: 可变参数列表
     * 
     * 工作流程：
     * 1. 获取当前时间（精确到微秒）
     * 2. 根据日志级别添加对应的标签（[debug]:、[info]: 等）
     * 3. 检查是否需要滚动日志文件（按天或按行）
     * 4. 格式化日志内容（时间戳 + 级别标签 + 用户消息）
     * 5. 根据模式写入：
     *    - 异步模式：将日志字符串推入阻塞队列
     *    - 同步模式：直接写入文件
     * 
     * 线程安全：
     * - 使用互斥锁保护所有共享资源的访问
     * - 确保多线程环境下的数据一致性
     */
    void write_log(int level, const char *format, ...);

    /*
     * =====================================================
     * 刷新日志缓冲区
     * =====================================================
     * 
     * 功能：
     * - 强制刷新文件流缓冲区
     * - 确保日志立即写入磁盘，而不是停留在内存缓冲区
     * 
     * 使用场景：
     * - 程序退出前，确保所有日志都已写入
     * - 重要日志记录后，立即持久化
     * - 异步模式下，由日志宏自动调用
     */
    void flush(void);

private:
    /*
     * =====================================================
     * 私有构造函数
     * =====================================================
     * 
     * 单例模式要求：
     * - 构造函数必须为私有
     * - 防止外部创建 Log 实例
     * - 只能通过 get_instance() 获取唯一实例
     */
    Log();
    
    /*
     * =====================================================
     * 私有析构函数
     * =====================================================
     * 
     * 功能：
     * - 关闭日志文件
     * - 释放相关资源
     * 
     * 单例模式要求：
     * - 析构函数必须为私有
     * - 防止外部删除实例
     */
    virtual ~Log();

    /*
     * =====================================================
     * 异步写日志的核心实现
     * =====================================================
     * 
     * 工作原理：
     * - 这是一个后台线程的执行体
     * - 无限循环，持续从阻塞队列中取出日志
     * - 将取出的日志写入文件
     * 
     * 生产者-消费者模式：
     * - 生产者：调用 write_log 的各个业务线程
     *           将日志推入阻塞队列
     * - 消费者：本函数所在的异步线程
     *           从队列取出日志并写入文件
     * 
     * 线程安全：
     * - 使用互斥锁保护文件写入操作
     * - 防止多线程同时写入导致数据错乱
     * 
     * 性能优势：
     * - 业务线程只需将日志推入队列（非阻塞或低阻塞）
     * - 真正的文件 I/O 由单独的线程完成
     * - 显著提高业务线程的响应速度
     */
    void *async_write_log()
    {
        string single_log;
        //从阻塞队列中取出一个日志string，写入文件
        // pop() 操作在队列为空时会阻塞等待
        while (m_log_queue->pop(single_log))
        {
            m_mutex.lock();                      // 加锁，保护文件写入
            fputs(single_log.c_str(), m_fp);    // 将日志写入文件
            m_mutex.unlock();                    // 解锁
        }
    }

private:
    /*
     * =====================================================
     * 成员变量说明
     * =====================================================
     */
    
    char dir_name[128];       // 日志文件所在目录名
                             // 用于日志文件滚动时构建新的完整路径
    
    char log_name[128];       // 日志文件名（不包含路径）
                             // 从用户传入的 file_name 中解析得到
    
    int m_split_lines;        // 日志文件最大行数阈值
                             // 当前日志文件的行数达到此值时，自动创建新文件
                             // 实现日志的按行滚动，防止单个文件过大
    
    int m_log_buf_size;       // 日志缓冲区大小（字节）
                             // 用于临时存储格式化后的日志内容
                             // 避免频繁的内存分配，提高性能
    
    long long m_count;        // 当前日志文件的行数计数器
                             // 每写入一条日志自增 1
                             // 用于判断是否需要按行滚动
    
    int m_today;              // 当前日期（几号）
                             // 用于实现按天滚动日志
                             // 当日期变化时，自动创建新的日志文件
    
    FILE *m_fp;               // 日志文件的文件指针
                             // 指向当前打开的日志文件
                             // 用于所有文件写入操作
    
    char *m_buf;              // 日志缓冲区指针
                             // 指向分配的内存缓冲区
                             // 用于格式化日志字符串
    
    block_queue<string> *m_log_queue;  // 阻塞队列指针（异步模式使用）
                                       // 类型为 block_queue<string>
                                       // 用于存储待写入的日志字符串
                                       // 是异步日志的核心组件
    
    bool m_is_async;          // 异步模式标志位
                             // true: 异步模式（使用阻塞队列）
                             // false: 同步模式（直接写入文件）
                             // 由 init() 函数中的 max_queue_size 参数决定
    
    locker m_mutex;           // 互斥锁对象
                             // 用于保护共享资源的线程安全访问
                             // 确保多线程环境下数据的一致性
    
    int m_close_log;          // 是否关闭日志标志
                             // 0: 开启日志（记录日志）
                             // 1: 关闭日志（不记录日志）
                             // 生产环境可通过此标志关闭日志，提升性能
};

/*
 * =====================================================
 * 日志级别宏定义
 * =====================================================
 * 
 * 设计目的：
 * 1. 简化日志记录的使用接口
 * 2. 提供类似 printf 的便捷语法
 * 3. 通过条件编译控制日志开关
 * 
 * 宏的工作原理：
 * 1. 检查 m_close_log 是否为 0（日志开启）
 * 2. 调用 get_instance() 获取单例
 * 3. 调用 write_log() 写入对应级别的日志
 * 4. 调用 flush() 确保日志写入磁盘
 * 
 * ##__VA_ARGS__ 的作用：
 * - 当可变参数为空时，删除前面的逗号
 * - 避免 "format, ," 这样的语法错误
 * 
 * 使用示例：
 * LOG_INFO("Server started on port %d", 8080);
 * LOG_ERROR("Connection failed: %s", strerror(errno));
 * LOG_WARN("Memory usage: %d MB", usage);
 * LOG_DEBUG("Debug info: value=%d", value);
 */
#define LOG_DEBUG(format, ...) if(0 == m_close_log) {Log::get_instance()->write_log(0, format, ##__VA_ARGS__); Log::get_instance()->flush();}
#define LOG_INFO(format, ...) if(0 == m_close_log) {Log::get_instance()->write_log(1, format, ##__VA_ARGS__); Log::get_instance()->flush();}
#define LOG_WARN(format, ...) if(0 == m_close_log) {Log::get_instance()->write_log(2, format, ##__VA_ARGS__); Log::get_instance()->flush();}
#define LOG_ERROR(format, ...) if(0 == m_close_log) {Log::get_instance()->write_log(3, format, ##__VA_ARGS__); Log::get_instance()->flush();}

#endif
